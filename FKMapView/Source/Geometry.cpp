void CEnumTileArea::EnumTileArea(const RECT& rcTileArea)
{
	// 初始化有效的TILE
	int nTileRow = rcTileArea.bottom;
	int nTileCol = rcTileArea.right*2;
	for (int row=0; row<nTileRow; row++) // 一个“之”字行
	{
		POINT ptCurTile;
		ptCurTile.x = rcTileArea.left + row;
		ptCurTile.y = rcTileArea.top - row;
		for (int col=0; col<nTileCol; col++) // 列数
		{
			if (retTrue != OnFoundTile(ptCurTile.x, ptCurTile.y))
				return;	
			
			ptCurTile.x += !(col&1); // odd
			ptCurTile.y += col&1;
		}
	}
}
