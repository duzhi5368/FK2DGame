/*
函数：	BeginDiffuse
功能：开始扩散的操作
参数：
		nX , nY		：扩散原点
		nR			：扩散最大半径

返回值：

注意：	
		1、假设地图的坐标左上角为（0，0）
		2、如果越界（即找到的新的点的坐标超出m_nWidth，m_nHeight所限制的范围），则
		不会对该点调用OnFoundTile函数
		3、如果子类的OnFoundTile返回了
*/
BOOL CDiffuseBase::BeginDiffuse( int nX , int nY )
{
	m_nOX = nX ;
	m_nOY = nY ;
	int nCircleIndex = 0 ; // 现在正在搜索第几圈？
	int x = 0 ;
	int y = 0 ;


	BOOL bIsCheckEdge = IsNeedCheckEdge() ; //是否需要边界检查？？

	// 遍历数组，输出每一圈的点
	for( int i=0 ; i<ARR_LEN ; )
	{
		nCircleIndex++ ; // 这是新的一圈,

		//1 是否可以结束了（调用子类定义的函数）？
		if( IsDiffuseComplete( nCircleIndex ) ) 
			return TRUE ;	// !!退出函数

		//2 告诉子类：已经开始新的一圈
		OnBeginNewCircle() ; //调用子类定义的函数便于子类做出响应

		//3 开始按自定义的格式得到数组中的点
		int iNum = g_arrAllCircle[i] ; // iNum 表示当前这一圈有多少个点
		for( int j=0 ; j<iNum ; j++ )
		{

			// 从数组中得到坐标（两个元素）
			x = g_arrAllCircle[i+j*2+1] ;
			y = g_arrAllCircle[i+j*2+2] ;

			int nRet = TryFindOneTile( x , y , j , iNum , nCircleIndex , 
								bIsCheckEdge , i , TRUE ) ;
			switch( retContinue )
			{
			case retContinue :
					continue ;
					break;
			case retFalse	 :
					return FALSE ;
					break;
			default:
				break ;
			}

		} // end for( j ......

		// 增加数组下标
		i = i + 1 + iNum*2 ; // 第一个“1”表示“表明该圈有多少个点的那个元素（它的值被读入iNum)”，后面“iNum*2”表示共有iNum个点，每个点占两个位置（X，Y坐标就是两个位置）
	}

	ResetCurMark(); // 每次扩散前必须初始化当前掩码的值，也有可能会初始化整个掩码数组
	return TRUE ;
} 
