// 初始化掩码值及其数组
void CDiffuseBase::ResetCurMark()
{
	m_nCurMark++;
	if( m_nCurMark<=0 ) // m_nCurMark溢出了
	{
		m_nCurMark = 1 ;
		memset( m_arrBlockedMark , 0 , sizeof(m_arrBlockedMark) ) ;
	}
}
//------------------------------------------------------------------------
// 把一个点所挡住的所有TILE的标志位使用掩码填充（m_arrBlockedMark中）
void CDiffuseBase::MarkAllBlockTile(int iPosInCircle		,	// 这个点在这一圈中的位置 
									int iNumOfCirclePoint	,	// 这一圈有多少个点
									int iCircleIndex		,	// 这是第几圈？
									int nHeadInArr			  ,// 这一圈第一个元素在数组中的位置
									BOOL	bIsSmallAngle ,
									BOOL bIsAntiClocekWise)	
{
	//1 得到经过这个点两侧的两条直线
	POINT arrLine1[100] ; // 该线上的点最多不会超过100个吧
	POINT arrLine2[100] ;
	int nNumOfLine1 = 0 ;
	int nNumOfLine2 = 0 ;
	int nIndex1 = 0 ; // 表示iPosInCircle两侧的点在该圈中的位置
	int nIndex2 = 0 ; // 表示iPosInCircle两侧的点在该圈中的位置

	// 得到这个点两侧（同一圈上）的点
	if( 0 == iPosInCircle ) // 该点为圈第一个点
	{
		nIndex1 = iNumOfCirclePoint - 1 ; // 最后一个点
		nIndex2 = 1 ;
	}
	else if( iNumOfCirclePoint - 1 == iPosInCircle ) // 该点为圈中最后一个点
	{
		nIndex1 = iNumOfCirclePoint - 2 ;// 最后一个点 
		nIndex2 = 0 ;
	}
	else
	{
		nIndex1 = iPosInCircle - 1 ;
		nIndex2 = iPosInCircle + 1 ;
	}

	int nX1 = g_arrAllCircle[ nHeadInArr + nIndex1*2 + 1 ] ;
	int nY1 = g_arrAllCircle[ nHeadInArr + nIndex1*2 + 2 ] ;
	int nX2 = g_arrAllCircle[ nHeadInArr + nIndex2*2 + 1 ] ;
	int nY2 = g_arrAllCircle[ nHeadInArr + nIndex2*2 + 2 ] ;

	GetLine( nX1 , nY1 , arrLine1 , nNumOfLine1 ) ;
	GetLine( nX2 , nY2 , arrLine2 , nNumOfLine2 ) ;

	//2 后面所有的圈中的该位置中的点都做“已遮挡”标记
	BlockBetween2Lines( arrLine1 , nNumOfLine1 , arrLine2 , nNumOfLine2 , iCircleIndex ,  bIsSmallAngle , bIsAntiClocekWise) ;
}
//------------------------------------------------------------------------
// 把两条直线之间夹角所包含的点标记上“已遮挡”
void CDiffuseBase::BlockBetween2Lines(  POINT *arrStartLine ,
										int nStartNumOfLine ,
									   POINT *arrEndLine ,
									   int nEndNumOfLine ,
										int nMinCircleIndex  ,
									BOOL	bIsSmallAngle ,
									BOOL bIsDrawCircleBlocked)
{
	int iCurCircleIndex = 0 ;
	int iCurCirclePointNum =  0 ; //m_arrAllCircle[ 0 ] ;
	int i = 0 ;
	while( i < ARR_LEN  )
	{
		iCurCircleIndex++ ; // 这是新的一圈,
		iCurCirclePointNum = g_arrAllCircle[ i ] ;
		if( iCurCircleIndex < nMinCircleIndex+1 )
		{
			i = i +1 + iCurCirclePointNum * 2 ;
			continue ;
		}
		
		BOOL bIsBlocked = FALSE ; // 这个初始化是有意义的
		BOOL bIsStartLineOver = FALSE ; // 是否已经经过一次vecStartLine了
		BOOL bIsDrawWhole = FALSE ;
		for( int j=0 ; j<iCurCirclePointNum ; j++ )
		{
			// 从数组中得到坐标（两个元素）
			int x = g_arrAllCircle[i+j*2+1] ;
			int y = g_arrAllCircle[i+j*2+2] ;

			BOOL IsInStartLine	= IsInLine( arrStartLine ,nStartNumOfLine, x , y ) ;
			BOOL IsInEndLine	= IsInLine( arrEndLine , nEndNumOfLine ,x , y ) ;

			if( IsInEndLine && IsInStartLine && (bIsDrawCircleBlocked) )
				 bIsDrawWhole = TRUE ;
			else if( IsInEndLine && IsInStartLine &&	bIsSmallAngle )
				break ;

			if( bIsDrawWhole )
				m_arrBlockedMark[ x ][ y ] = m_nCurMark ;

			//1 经过EndLine?
			if(	IsInEndLine  )
			{
				if( bIsStartLineOver )
				{
					bIsBlocked = FALSE ;
					continue ;
				}
				else
				{
					j = -1 ; //再循环一次
					bIsBlocked = TRUE ;
					bIsStartLineOver = TRUE ;
					continue ;
				}
			}

		//1 经过StartLine?
		if( IsInStartLine && (!bIsStartLineOver ) ) 
			bIsStartLineOver = TRUE ;

		if( IsInStartLine  )
			{
				bIsBlocked = TRUE ;
				continue ;
			}

			if( bIsBlocked)	m_arrBlockedMark[ x ][ y ] = m_nCurMark ;
		}

		i = i +1 + iCurCirclePointNum * 2 ;
	}
}
//------------------------------------------------------------------------
// 判断点(x,y)是否在线pVecLine1~pVecLine2之间的夹角之中
BOOL CDiffuseBase::IsInLine( POINT *arrLine , int nNumOfLine , int nX , int nY )
{
	for( int i=0 ; i<nNumOfLine ; i++ )
	{
		int nXInLine = arrLine[i].x ;
		int nYInLine = arrLine[i].y ;
		if( nXInLine == nX && nYInLine == nY)
		{
			return TRUE ;
		}
	}

	return FALSE ;
}
//------------------------------------------------------------------------
void CDiffuseBase::GetLine( int nX , int nY , POINT *arrLine , int &nNumOfLine )
{
	int step_x,step_y,dx,dy,dxa,dyb,x,y,pk,twodxa,twodyb;
	int x1 = ARR_CEN_X  ;
	int y1 = ARR_CEN_Y  ;
	int x2 = nX ;
	int y2 = nY ;
	x = x1 ;	y = y1;
	dx = x2 - x1; dy = y2 - y1;
	x2 = x2>ARR_CEN_X ? ARR_CEN_X*2 : 0 ;
	y2 = y2>ARR_CEN_Y ? ARR_CEN_Y*2 : 0 ;


	if (dx > 0)	
	{
		step_x = 1; dxa = dx;
	}
	else if (dx < 0)
	{
		step_x = -1; dxa = -dx;
	}
	else // 垂直线
	{
		if (dy > 0)
			step_y = 1;
		else if (dy < 0)
			step_y = -1;
		else // 两个点重叠
			return;

		while (y != y2)
		{
			y += step_y;
		
			InsertPoint(x,y,arrLine ,nNumOfLine) ;// 把该点记录到列表中去
		}
		return;
	}

	if (dy > 0)
	{
		step_y = 1;
		dyb = dy;
	}
	else if (dy < 0)
	{
		step_y = -1;
		dyb = -dy;
	}
	else // 水平线
	{
		while (x != x2)
		{
			x += step_x;
			InsertPoint(x,y,arrLine ,nNumOfLine) ;// 把该点记录到列表中去
		}
		return;
	}

	twodxa = dxa<<1;
	twodyb = dyb<<1;
	if (dxa >= dyb)
	{
		int twodyb_dxa = twodyb - twodxa;
		pk = twodyb - dxa;
		while (x != x2)
		{
			x += step_x;
			if (pk < 0)
				pk += twodyb;
			else
			{
				pk += twodyb_dxa;
				y += step_y;
				InsertPoint( x  , y - step_y ,arrLine ,nNumOfLine ) ; // 为了保持线的连续（垂直/水平方面都连续）
			}
			InsertPoint(x,y,arrLine ,nNumOfLine ) ; // 把该点记录到列表中去
		}
	}
	else
	{
		int twodxa_dyb = twodxa - twodyb;
		pk = twodxa - dyb;
		while (y != y2)
		{
			y += step_y;
			if (pk < 0)
				pk += twodxa;
			else
			{
				pk += twodxa_dyb;
				x += step_x ; // YuHaiyun
				InsertPoint( x - step_x , y,arrLine ,nNumOfLine ) ; // 为了保持线的连续（垂直/水平方面都连续）
			}
			InsertPoint(x,y,arrLine ,nNumOfLine ); // 把该点记录到列表中去
		}
	}
}
//------------------------------------------------------------------------
void CDiffuseBase::InsertPoint( int nX , int nY , POINT *arrPoint , int &nNumOfLine )
{
	POINT point = { nX , nY } ;
	
	if( arrPoint )
	{
		arrPoint[nNumOfLine] = point ;
		nNumOfLine ++ ;
	}
}
